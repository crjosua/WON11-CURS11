package Exercitiul3;

 public class Person {
    private String name;
    private int age;
    private String hair;
    public Person(String name, int age, String hair) {
        this.name = name;
        this.age = age;
        this.hair = hair;
    }
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public String getHair() {
        return hair;
    }
}


